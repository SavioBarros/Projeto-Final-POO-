    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        
        if(tfTitulo.getText().equals("") || tfAutor.getText().equals("") || tfISBN.getText().equals("") || tfDescricao.getText().equals("") ){
            
            JOptionPane.showMessageDialog(null, "Campos Inv√°lidos!");
        }
        
        else {
            
            Livros livro = new Livros();
            BibliotecariaDAO dao = new BibliotecariaDAO();
        
            livro.setTituloLivro(tfTitulo.getText());
            livro.setAutor(tfAutor.getText());
            livro.setIsbn(Integer.parseInt(tfISBN.getText()));
            livro.setDescricao(tfDescricao.getText());
        
            try {
                dao.addLivros(livro);
                 JOptionPane.showMessageDialog(null, "Livro Cadastrado!");
            } catch (LJCException ex) {
                
               JOptionPane.showMessageDialog(this, ex.getMessage(), "ERRO", JOptionPane.ERROR_MESSAGE);
            }
        
           
        
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

