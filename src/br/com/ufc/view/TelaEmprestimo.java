    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        
        if(tfID.getText().isEmpty() || tfAluno.getText().isEmpty() || tfLivro.getText().isEmpty()) {
            
            JOptionPane.showMessageDialog(this, "Preencha todos os campos", "Erro", JOptionPane.ERROR_MESSAGE);
            
        } else {
            
            int idEmprestimo;
            Emprestimo emprestimos = new Emprestimo();
            
            try {
                idEmprestimo = Integer.parseInt(tfID.getText());

                try {
                    daoEmprestimo.buscarPorId(Integer.parseInt(tfID.getText()));
                } catch (ENEException ex) {
                    
                    Aluno aluno = dao.buscarPorMatricula(Integer.parseInt(tfAluno.getText()));
                    Livros livro = daoLivro.pesquisar(Integer.parseInt(tfLivro.getText()));
                    Date hoje = new Date();
                    emprestimos.setDataEmprestimo(hoje);

                    Date devolucao = new Date();
                    devolucao.setDate(devolucao.getDate() + 15);
                    emprestimos.setDataDevolucao(devolucao);

                    emprestimos.setId(idEmprestimo);
                    emprestimos.setLivro(livro);
                    emprestimos.setAluno(aluno);

                    try {
                        daoEmprestimo.adicionar(emprestimos);
                        JOptionPane.showMessageDialog(null, "Emprestimo realizado!");
                    } catch (ENEException ex1) {
                        JOptionPane.showMessageDialog(this, ex.getMessage());
                    }
                }

                
            } catch(NumberFormatException ex) {
                JOptionPane.showMessageDialog(this, "Insira um valor válido", "Erro", JOptionPane.ERROR_MESSAGE);
            } catch (ANEException ex) {
                JOptionPane.showMessageDialog(this, ex.getMessage());
            } catch (LNEException ex) {
                JOptionPane.showMessageDialog(this, ex.getMessage());
            } catch (LLUException ex) {
                JOptionPane.showMessageDialog(this, ex.getMessage());
            } catch (EJCException ex) {
                JOptionPane.showMessageDialog(this, ex.getMessage());
            }   
        }
    }//GEN-LAST:event_jButton6ActionPerformed

