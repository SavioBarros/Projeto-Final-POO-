    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
       
        if(campoEmprestimo.getText().isEmpty()){
            
            JOptionPane.showMessageDialog(this, "Preencha o campo!", "ERRO", JOptionPane.ERROR_MESSAGE);
        }
        
        else {
            
            int id = Integer.parseInt(campoEmprestimo.getText());
            
            try{
                
                daoEmprestimo.remover(id);
                JOptionPane.showMessageDialog(this, "Devolução realizada com sucesso!");
            } catch (ENEException ex) {
                JOptionPane.showMessageDialog(this, ex.getMessage());
            }
        }
        
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
       
        if(campoEmprestimo.getText().isEmpty()){
            JOptionPane.showMessageDialog(this,"Preencha o campo!", "ERRO", JOptionPane.ERROR_MESSAGE);
        }
        
        else{
            
            try {
                Emprestimo e = daoEmprestimo.buscarPorId(Integer.parseInt(campoEmprestimo.getText()));
                daoEmprestimo.renovarEmprestimo(e);
                JOptionPane.showMessageDialog(this, "Emprestimo renovado com sucesso!");
            } catch (ENEException | EJRException ex) {
                JOptionPane.showMessageDialog(this, ex.getMessage());
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed


