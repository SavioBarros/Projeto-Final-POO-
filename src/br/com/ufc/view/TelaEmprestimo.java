    public void mostrarEmprestimo() {
        
       DefaultTableModel dados = (DefaultTableModel) jTable1.getModel();
        
        while(dados.getRowCount() > 0){
            
            dados.removeRow(0);
        }    
        
        List<Emprestimo> emprestimos = daoEmprestimo.mostrarTodos();
        
        for(Emprestimo emp : emprestimos){
            
            dados.addRow(new Object[] {emp.getId(),emp.getAluno().getNome(),emp.getLivro().getTituloLivro(),emp.getDataEmprestimo(),emp.getDataDevolucao()});
        }
    }

