    private void jLabel2MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel2MouseClicked
       
        if(tfNome.getText().equals("") || tfMatricula.getText().equals("") || jftfCPF.getText().equals("") ||
            tfCurso.getText().equals("") || jftfNascimento.getText().equals("") || jftfTelefone.getText().equals("")){
            
            JOptionPane.showMessageDialog(null, "Campos Invalidos!");
            
        }
        else {
            
            Aluno alunos = new Aluno();
            BibliotecariaDAOAluno dao = new BibliotecariaDAOAluno();
        
            alunos.setNome(tfNome.getText());
            alunos.setMatricula(Integer.parseInt(tfMatricula.getText()));
            alunos.setCpf(jftfCPF.getText());
            alunos.setCurso(tfCurso.getText());
            alunos.setDataNascimento(jftfNascimento.getText());
            alunos.setTelefone(jftfTelefone.getText());
        
            try {
                dao.addAluno(alunos);
                JOptionPane.showMessageDialog(null, "Cadastrado com sucesso!");
                
            } catch (AJCException ex) {
                JOptionPane.showMessageDialog(this, ex.getMessage(), "ERRO", JOptionPane.ERROR_MESSAGE);
            }
            dao.mostrar();
            
        
        }
        
    }//GEN-LAST:event_jLabel2MouseClicked

